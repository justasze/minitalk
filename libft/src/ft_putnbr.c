#include "libft.h"

void	ft_putnbr(int n)
{
	int		neg;

	neg = 1;
	if (n < 0)
	{
		ft_putchar('-');
		neg = -1;
	}
	if (n >= 10 | n <= -10)
		ft_putnbr((n / 10) * neg);
	ft_putchar((char)((n % 10) * neg) + '0');
}
