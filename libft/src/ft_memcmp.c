#include "libft.h"

int		ft_memcmp(void *s1, void *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (*((unsigned char *)s1 + i) != *((unsigned char *)s2 + i))
		{
			return ((int)*((unsigned char *)s1 + i)
					- *((unsigned char *)s2 + i));
		}
		i++;
	}
	return (0);
}
