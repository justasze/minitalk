#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		neg;

	neg = 1;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		neg = -1;
	}
	if (n >= 10 | n <= -10)
		ft_putnbr_fd((n / 10) * neg, fd);
	ft_putchar_fd((char)((n % 10) * neg) + '0', fd);
}
