#include "libft.h"

int			ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		result;

	result = 0;
	neg = 1;
	i = 0;
	while (str[i] && ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		result *= 10;
		result += (str[i] - '0') * neg;
		i++;
	}
	return (result);
}
